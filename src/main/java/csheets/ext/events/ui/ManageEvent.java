    private void btnCreate(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreate

		Calendar calendar = DateTime.
			newCalendar(Integer.parseInt(this.jTextFieldYear.getText()), Integer.
						parseInt(this.jTextFieldMonth.getText()), Integer.
						parseInt(this.jTextFieldDay.getText()), Integer.
						parseInt(this.jTextFieldHour.getText()), Integer.
						parseInt(this.jTextFieldMin.getText()), 0);
		try {
			this.controller.
				createEvent((Contact) this.jComboBoxContacts.getSelectedItem(), this.jTextFieldEventName.
							getText(), calendar);
		} catch (DataIntegrityViolationException ex) {
			System.out.println("Evento j√° existe");
		}
		dispose();
    }//GEN-LAST:event_btnCreate

