    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

		String script_name = JOptionPane.
			showInputDialog("Introduces a name for this function script?");

		String code = txtAreaCode.getText();

		if (!code.equals("") || !script_name.equals("")) {
			controller.saveScript(script_name, code);
			managerUI.displayScripts();
			txtAreaCode.setText(null);
			this.dispose();
		} else {
			JOptionPane.
				showMessageDialog(this, "You didn't create any script.", "Error", JOptionPane.ERROR_MESSAGE);
		}
    }//GEN-LAST:event_btnSaveActionPerformed

