    private void jPanelColorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanelColorMouseClicked
		JColorChooser colorChooser = new JColorChooser();
		colorChooser.setColor(this.jPanelColor.getBackground());
		JPanel panel = new JPanel();
		panel.add(colorChooser);
		int result = JOptionPane.
			showConfirmDialog(null, panel, "Please choose the color for the calendar:", JOptionPane.OK_CANCEL_OPTION);
		if (result == JOptionPane.OK_OPTION) {
			this.jPanelColor.setBackground(colorChooser.getColor());
		}
    }//GEN-LAST:event_jPanelColorMouseClicked

