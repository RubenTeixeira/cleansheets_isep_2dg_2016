    private void jButtonCreateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonCreateMouseClicked
		try {
			this.controller.
				createCalendar(jTextFieldName.getText(), jTextFieldDescription.
							   getText(), jPanelColor.getBackground(), (Contact) jComboBoxContact.
							   getSelectedItem());
			this.dispose();
		} catch (DataIntegrityViolationException ex) {
			Logger.getLogger(CalendarManage.class.getName()).
				log(Level.SEVERE, null, ex);
		}
    }//GEN-LAST:event_jButtonCreateMouseClicked

