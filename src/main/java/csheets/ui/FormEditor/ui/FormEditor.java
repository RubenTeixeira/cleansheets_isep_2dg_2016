    private void refreshUI() {
        jPanel1.revalidate();
        jPanel1.repaint();
    }

    @Override
    public void update(Observable o, Object o1) {
       if(o1 instanceof SingleLine)
       {
           removeSingleLine((SingleLine)o1);
           refreshUI();
       }
    }

    
